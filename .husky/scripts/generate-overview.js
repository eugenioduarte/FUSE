const fs = require('fs')
const path = require('path')

const ROOT = path.resolve(__dirname, '../..')
const DOCS_DIR = path.join(ROOT, 'docs')
const OUTPUT = path.join(DOCS_DIR, 'project-overview.md')
const SRC = path.join(ROOT, 'src')
const APP = path.join(ROOT, 'app')

if (!fs.existsSync(DOCS_DIR)) fs.mkdirSync(DOCS_DIR, { recursive: true })

// ── Helpers ──────────────────────────────────────────────────

function listDir(dir, ext = ['.ts', '.tsx']) {
  if (!fs.existsSync(dir)) return []
  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
}

function countFiles(dir, exts = ['.ts', '.tsx']) {
  if (!fs.existsSync(dir)) return 0
  let count = 0
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) count += countFiles(full, exts)
    else if (exts.some((e) => entry.name.endsWith(e))) count++
  }
  return count
}

function listLeafDirs(base, depth = 2) {
  if (!fs.existsSync(base)) return []
  const results = []
  function walk(dir, d) {
    const entries = fs.readdirSync(dir, { withFileTypes: true })
    const subdirs = entries.filter(
      (e) =>
        e.isDirectory() &&
        !e.name.startsWith('.') &&
        e.name !== '__tests__' &&
        e.name !== 'node_modules',
    )
    if (subdirs.length === 0 || d === 0) {
      results.push(path.relative(base, dir))
    } else {
      subdirs.forEach((s) => walk(path.join(dir, s.name), d - 1))
    }
  }
  walk(base, depth)
  return results.filter(Boolean)
}

function extractAnnotation(filePath) {
  if (!fs.existsSync(filePath)) return null
  const firstLine = fs.readFileSync(filePath, 'utf8').split('\n')[0]
  if (firstLine.includes('@last-modified:'))
    return firstLine.replace('//', '').trim()
  return null
}

function getScreens() {
  const screensDir = path.join(SRC, 'screens')
  if (!fs.existsSync(screensDir)) return []
  const rows = []
  for (const domain of fs.readdirSync(screensDir, { withFileTypes: true })) {
    if (!domain.isDirectory()) continue
    const domainPath = path.join(screensDir, domain.name)
    for (const screen of fs.readdirSync(domainPath, { withFileTypes: true })) {
      if (!screen.isDirectory()) continue
      const screenPath = path.join(domainPath, screen.name)
      const hookFile = path.join(screenPath, `${screen.name}.hook.ts`)
      const annotation = extractAnnotation(
        path.join(screenPath, `${screen.name}.screen.tsx`),
      )
      rows.push({ domain: domain.name, screen: screen.name, annotation })
    }
  }
  return rows
}

function getComponents() {
  const compDir = path.join(SRC, 'components')
  if (!fs.existsSync(compDir)) return []
  const results = []
  function walk(dir) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name)
      if (entry.isDirectory()) {
        walk(full)
        continue
      }
      if (entry.name.endsWith('.tsx') || entry.name.endsWith('.ts')) {
        const annotation = extractAnnotation(full)
        results.push({
          file: path.relative(path.join(SRC, 'components'), full),
          annotation,
        })
      }
    }
  }
  walk(compDir)
  return results
}

function getRoutes() {
  if (!fs.existsSync(APP)) return []
  const routes = []
  function walk(dir, prefix = '') {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name)
      if (entry.isDirectory()) walk(full, `${prefix}/${entry.name}`)
      else if (entry.name === '_layout.tsx')
        routes.push(`${prefix || '/'} → _layout`)
      else if (entry.name.endsWith('.tsx'))
        routes.push(`${prefix}/${entry.name.replace('.tsx', '')}`)
    }
  }
  walk(APP)
  return routes
}

// ── Build markdown ────────────────────────────────────────────

const now = new Date().toISOString().split('T')[0]
const screens = getScreens()
const components = getComponents()
const routes = getRoutes()

const storeSlices = listLeafDirs(path.join(SRC, 'store'), 2)
const serviceApis = listLeafDirs(path.join(SRC, 'services', 'api'), 1)
const serviceQueries = listLeafDirs(path.join(SRC, 'services', 'query'), 1)
